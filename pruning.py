import torch
import torch.nn as nn
import torch.nn.functional as F
import torchvision
import torchvision.transforms as transforms
from torch.utils.data import DataLoader
import matplotlib.pyplot as plt

# ── Custom Prunable Linear Layer ──────────────────────────────
class PrunableLinear(nn.Module):
    def __init__(self, in_features, out_features):
        super().__init__()
        self.weight = nn.Parameter(torch.randn(out_features, in_features) * 0.01)
        self.bias   = nn.Parameter(torch.zeros(out_features))
        # Gate scores: same shape as weight, also learned by optimizer
        self.gate_scores = nn.Parameter(torch.ones(out_features, in_features))

    def forward(self, x):
        gates         = torch.sigmoid(self.gate_scores)   # squash to (0,1)
        pruned_weights = self.weight * gates               # element-wise mask
        return F.linear(x, pruned_weights, self.bias)

    def get_gates(self):
        return torch.sigmoid(self.gate_scores.detach())


# ── Full Network using PrunableLinear ─────────────────────────
class SelfPruningNet(nn.Module):
    def __init__(self):
        super().__init__()
        # CIFAR-10: 3 channels × 32×32 = 3072 inputs, 10 classes
        self.fc1 = PrunableLinear(3072, 512)
        self.fc2 = PrunableLinear(512, 256)
        self.fc3 = PrunableLinear(256, 10)
        self.relu = nn.ReLU()

    def forward(self, x):
        x = x.view(x.size(0), -1)        # flatten image
        x = self.relu(self.fc1(x))
        x = self.relu(self.fc2(x))
        return self.fc3(x)

    def sparsity_loss(self):
        # L1 norm of all gate values across every PrunableLinear layer
        total = 0
        for m in self.modules():
            if isinstance(m, PrunableLinear):
                total += m.get_gates().abs().sum()
        return total

print("✅ Classes defined successfully")

transform = transforms.Compose([
    transforms.ToTensor(),
    transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5))
])

trainset = torchvision.datasets.CIFAR10(root='./data', train=True,
                                         download=True, transform=transform)
testset  = torchvision.datasets.CIFAR10(root='./data', train=False,
                                         download=True, transform=transform)

trainloader = DataLoader(trainset, batch_size=128, shuffle=True,  num_workers=2)
testloader  = DataLoader(testset,  batch_size=256, shuffle=False, num_workers=2)

print("✅ CIFAR-10 loaded:", len(trainset), "train |", len(testset), "test samples")

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print(f"✅ Using device: {device}")

def train_model(lam, epochs=15):
    model     = SelfPruningNet().to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=1e-3)
    criterion = nn.CrossEntropyLoss()

    for epoch in range(epochs):
        model.train()
        running_loss = 0
        for inputs, labels in trainloader:
            inputs, labels = inputs.to(device), labels.to(device)
            optimizer.zero_grad()

            outputs  = model(inputs)
            cls_loss = criterion(outputs, labels)
            sp_loss  = model.sparsity_loss()
            loss     = cls_loss + lam * sp_loss   # Total Loss

            loss.backward()
            optimizer.step()
            running_loss += loss.item()

        avg = running_loss / len(trainloader)
        print(f"  [λ={lam}] Epoch {epoch+1:02d}/{epochs} | Loss: {avg:.4f}")

    return model


def evaluate_model(model, lam, threshold=1e-2):
    model.eval()
    correct, total = 0, 0

    with torch.no_grad():
        for inputs, labels in testloader:
            inputs, labels = inputs.to(device), labels.to(device)
            outputs = model(inputs)
            _, predicted = outputs.max(1)
            correct += predicted.eq(labels).sum().item()
            total   += labels.size(0)

    accuracy = 100 * correct / total

    # Collect all gate values
    all_gates = []
    for m in model.modules():
        if isinstance(m, PrunableLinear):
            all_gates.append(m.get_gates().cpu().flatten())
    all_gates = torch.cat(all_gates)

    sparsity = (all_gates < threshold).float().mean().item() * 100

    print(f"\n  ✅ λ={lam} → Accuracy: {accuracy:.2f}% | Sparsity: {sparsity:.2f}%\n")
    return accuracy, sparsity, all_gates

print("✅ Functions ready")

lambdas = [1e-5, 1e-4, 1e-3]   # low / medium / high sparsity pressure
results = []

for lam in lambdas:
    print(f"\n{'='*45}")
    print(f"  Training with λ = {lam}")
    print(f"{'='*45}")
    model = train_model(lam, epochs=15)
    acc, sp, gates = evaluate_model(model, lam)
    results.append((lam, acc, sp, gates))


# ── Results Table ─────────────────────────────────────────────
print("\n| Lambda  | Test Accuracy | Sparsity Level |")
print("|---------|--------------|----------------|")
for lam, acc, sp, _ in results:
    print(f"| {lam:.0e}  | {acc:.2f}%        | {sp:.2f}%          |")


# ── Gate Distribution Plot ────────────────────────────────────
# Pick the model with highest accuracy for the plot
best = max(results, key=lambda r: r[1])

plt.figure(figsize=(9, 4))
plt.hist(best[3].numpy(), bins=100, color='steelblue', edgecolor='black')
plt.title(f"Gate Value Distribution — Best Model (λ={best[0]})", fontsize=13)
plt.xlabel("Gate Value (0 = pruned, 1 = fully active)")
plt.ylabel("Number of Weights")
plt.tight_layout()
plt.savefig("gate_distribution.png", dpi=150)
plt.show()
